        <span style="font-weight: bold;">accessibilité numérique et langue des signes</span> <br><br>
    
        Lors de l'élaboration d'un projet sur l'accessibilité numérique orientée
        sur la surdité, la question s'est posé de savoir quels était les meilleurs façons
        de retranscrire sons et paroles dans des contenus audio et vidéos.<br><br> 
        
        Les sous-titres et les closed captions sont traités dans un autre article.<br> <a href="https://promo-68.codeur.online/blog/index.php/author/nicolasm/">Ecris par NICOLASM</a>
