    <p>
        <span style="font-weight: bold;">grammaire de la LSF</span> <br><br>

        La langue des signes française permet d'exprimer plusieurs idées simultanément, ce qui la différencie de la grammaire française classique. <br><br>

        L'expressions du visage peut indiquer le sens de la phrase (par exemple pour dire qu'il s'agit d'une question). <br><br>

        Il n’existe pas de conjugaison en LSF, il suffit au signeur de situer l’action sur la ligne du temps 
        (perpendiculaire à lui:  derrière son épaule le passé, au niveau de son corps le présent et devant lui le futur). <br><br>

        Des gestes montres le rôle ou la forme du référent: Pour faire  référence à un Homme, le locuteur utilisera son index vers le  haut,mais pour parler d’une voiture, il utilisera la main à plat. Les verbes de mouvement peuvent également être  signés pour indiquer  la direction : <br><br>
            
        « La voiture tourne à gauche »  sera le classificateur de voiture tournant à gauche. Autre exemple,« Un homme monte l’escalier » se fera par deux doigts imitant les jambes montant un escalier, dans un mouvement pouvant être différent selon qu’il s’agit d’un escalier classique ou en colimaçon. <br><br>

        Pour parler de personnes absentes le signeur définit sa place et celle des autres dans l’espace, à la  manière d’une mise en scène théâtrale, il est ainsi plus facile de  comprendre et de suivre visuellement de qui il s’agit et quels sont les rapports entre les personnages. <br><br>

        En bref, la LSF est très riche et permet une communication très complète, qui viens en quelque sorte combler par le visuel le manque à gagner auditif.
    </p>

    <p>
        <span style="font-weight: bold;">incrustation vidéo</span> <br><br>
        
        Même si on est très loin d'une pratique systématique, on a tous déjà vu des vidéos, notamment de discours officiels,
        avec incrustation dans un coin de l'écran d'une personne traduisant les dialogues en temps réel. <br><br>
        
        Comme vu précédemment, cela permet a priori de retranscrire plus fidèlement le discours que par les sous-titres. En effet, et notamment dans le cadre d'une traduction instantanée, dure d'imaginer retranscrire par des sous-titres, les subtilités de la langue parlée.Tout simplement car l'écrit n'est pas fait pour cela : le descriptif complet des subtilités du language oral, incluant<br> les intonations par exemple, serait trop long pour du temps réel. <br><br><br>
        

        <span style="font-weight: bold;">applications utilisant la langue des signes</span> <br><br>

        Pour l'instant, il n'existe pas ou peut d'application pour sourds utilisant la langue des signes.
        Il existe en revanche des  applications de sous-titrage (comme rogervoice ou ava)
        ou des applications de traîtement de son (a destination des mal-entendants).<br><br>

        Cela est sans doute dû au fait que retranscrire une conversationen LSF via un algorithme serait
        techniquement plus compliquer que  généré des sous-titres : en effet, il faudrait pour cela analyser
        des information autre que les seuls mots, et la traduction passerait non-pas par des caractères mais
        par exemple par une animation complexe, impliquant des mouvement de main mais aussi de tout le corps, incluant des expressions faciales.
    </p> 

         <h8>idées pour le projet << accessibilité numérique >></h8>
        
        <p> Une idée serait de créer une interface semblable à celle d'un traducteur
         comme <br>celui de google, qui traduirait en alphabet dactylologique.
        <br><br>Celui-ci serait principalement à destination des personnes entendantes ayant <br>
        besoin d'un coup de pouce pour communiquer avec une personne sourde.
        <br><br>Un tel traducteur permettrait de constituer un mot bien plus rapidement qu'en 
        <br>ayant sous les yeux l'habituel fiche alphabétique.
