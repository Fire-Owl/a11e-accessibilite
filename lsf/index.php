                <h2>historique de la LSF</h2>
                <p class="test_para">
                    La LSF (langue des signes française) a longtemps été combattu et même explicitement <br>interdite, 
                    notamment car perçu par les autorités comme un "patois gestuel" (voir loi <br>du congrès de  Milan de 1880).
                    Ce n'est qu'en 1991 que l'interdiction de la LSF est<br> levé dans l'éducation, 
                    et celle-ci sera reconnut officiellement comme  une langue<br> à part entière en 2005.
                </p>
            </div>

