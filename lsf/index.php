            <h2>spécificités de la LSF</h2>
        
            <h4>Alphabet dactylologique</h4>
            <p class="test_para">
                L’alphabet dactylologique est la façon de signer l’alphabet latin. Il est utilisé pour épeler<br> les noms propres ou les mots n’existant pas encore en LSF.La dactylologie de la<br> LSF se fait d’une seule main.
            </p>
